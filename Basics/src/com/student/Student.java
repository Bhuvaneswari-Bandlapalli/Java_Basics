package com.student;

public class Student {
	private int sht;
	private String sname;
	private int Fl;
	private int sl;
	private int tl;
	private int maths;
	private int scie;
	private int socio;
	
	public int getSht() {
		return sht;
	}
	public void setSht(int sht) {
		this.sht = sht;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public int getFl() {
		return Fl;
	}
	public void setFl(int fl) {
		Fl = fl;
	}
	public int getSl() {
		return sl;
	}
	public void setSl(int sl) {
		this.sl = sl;
	}
	public int getTl() {
		return tl;
	}
	public void setTl(int tl) {
		this.tl = tl;
	}
	public int getMaths() {
		return maths;
	}
	public void setMaths(int maths) {
		this.maths = maths;
	}
	public int getScie() {
		return scie;
	}
	public void setScie(int scie) {
		this.scie = scie;
	}
	public int getSocio() {
		return socio;
	}
	public void setSocio(int socio) {
		this.socio = socio;
	}
	
	
	
	
}
