package com.decisionMaking_statement;

public class EvenOrOdd {
	/*
	 * if-else statement:-two way conditional statement that decides the execution path based on whether condition t/f.
	 if(condition){
	    statements:-executes when condition true.
	 }
	 else{
	     statements:-executes when condition false.
	  }
	 
	 */
	public static void main(String[] args) {
		int number=19;
		if(number%2==0) {
			System.out.println(number+" is Even Number.");
		}
		else {
			System.out.println(number+" is Odd number.");
		}
		

	}

}
